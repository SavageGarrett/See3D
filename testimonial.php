    <script>
      function changeTestimonialLength(index) {
        var shortTestimonies = document.getElementsByClassName('readLess');
        var longTestimonies = document.getElementsByClassName('readMore');
        var short = shortTestimonies[index];
        var long = longTestimonies[index];
        if (short.style.display != "none") {
          short.style.display = "none";
          long.style.display = "block";
        } else {
          short.style.display = "block";
          long.style.display = "none";
        }
        var readButtons = document.getElementsByClassName('read');
        var readButton = readButtons[index];
        if (readButton.innerHTML == "Read More...") {
          readButton.innerHTML = "Read Less...";
        } else {
          readButton.innerHTML = "Read More...";
        }
      }
    </script>

